/*
    Ternary Operator = A shortcut to if{} and else{} statements helps to assign a variable based on a condition ? codeIfTrue : codeIfFalse;
*/
let age = 21;
let mensage = age >= 18 ? "You're an adult" : "You're a minor";
console.log(mensage);

let time = 16;
let greeting = time <= 12 ? "Good Morning!" : "Good Afternoon!";
console.log(greeting);

let isStudent = false;
let mensage2 = isStudent ? "You're a student" : "You're not student";
console.log(mensage2);